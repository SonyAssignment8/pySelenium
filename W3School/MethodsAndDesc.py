x = "hello123Atul"
print("1.", x.capitalize())  # Converts the first character to upper case like h to H
print("2.", x.casefold())  # Converts string into lower case
print("3.", x.center(5))  # Returns a centered string--------NA
print("4.", x.count("l"))  # Returns the number of times a specified value occurs in a string
print("5.", x.title())
print("6.", x.encode())  # Returns an encoded version of the string
print("7.", x.endswith("l"))  # Returns true if the string ends with the specified value
print("8.", x.expandtabs())  # Sets the tab size of the string----NA
print("9.", x.find("2"))  # Searches the string for a specified value and returns the position of where it was found
print("10.", x.format())
print("11.", x.isalnum())  # Formats specified values in a string
print("12.", x.isprintable())  # Returns True if all characters in the string are printable
print("13.", x.ljust(8))  # Returns a left justified version of the string
print("14", x.upper())
print("15", x.zfill(3))  # Fills the string with a specified number of 0 values at the beginning
print("16", len(x))